package Viewer;

public class LoopTest {
	public static void main(String[] args) {
		int sum = 0;
		for(int i = 0; i <= 10; i++ ) {
			if(i != 10) {
				System.out.printf("%d + ", i);
			}else{
			System.out.printf("%d = ", i);
			}
			sum += i;			
		}
		System.out.print(sum);
		
		
			
		
		
		// 1 + 2 = 3
		int i = 0;
		while( i< 10) {
				i++;
		}
		
	}
	
}
